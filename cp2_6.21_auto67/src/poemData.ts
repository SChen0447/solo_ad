export function getRandomPoems(limit: number = 10, excludeIds: string[] = []): Poem[] {
  const available = poems.filter(p => !excludeIds.includes(p.id));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  const result = shuffled.slice(0, Math.min(limit, shuffled.length));
  
  if (result.length < limit && result.length < available.length) {
    const remaining = limit - result.length;
    const usedIds = result.map(p => p.id);
    const more = poems.filter(p => !usedIds.includes(p.id));
    const shuffledMore = [...more].sort(() => Math.random() - 0.5);
    result.push(...shuffledMore.slice(0, remaining));
  }
  
  return result;
}

export function filterByDynasty(dynasty: string): Poem[] {
  if (!dynasty) return [...poems];
  return poems.filter(p => p.dynasty === dynasty);
}

export function searchPoems(query: string, dynasty: string = ''): Poem[] {
  const normalized = query.trim().toLowerCase();
  let pool = poems;
  
  if (dynasty) {
    pool = pool.filter(p => p.dynasty === dynasty);
  }
  
  if (!normalized) return pool;
  
  return pool.filter(p => 
    p.title.toLowerCase().includes(normalized) ||
    p.author.toLowerCase().includes(normalized) ||
    p.content.some(line => line.toLowerCase().includes(normalized))
  );
}

export function getAllPoems(): Poem[] {
  return [...poems];
}

export function getTotalCount(): number {
  return poems.length;
}
