COMMON_FORBIDDEN_PATTERNS = [
    "相同", "类似", "像", "是", "就是", "等于", "英语", "英文",
    "怎么说", "怎么读", "怎么写", "拼音", "笔画", "部首",
]


class WordLibrary:
    def __init__(self):
        self.used_words: Dict[str, set] = {}

    def get_random_card(self, theme: str, room_id: str) -> Tuple[str, List[str]]:
        if theme not in THEME_LIBRARY:
            theme = "fantasy"

        if room_id not in self.used_words:
            self.used_words[room_id] = set()

        available = [w for w in THEME_LIBRARY[theme] if w[0] not in self.used_words[room_id]]

        if not available:
            self.used_words[room_id] = set()
            available = THEME_LIBRARY[theme]

        card = random.choice(available)
        self.used_words[room_id].add(card[0])
        return card[0], list(card[1])

    def generate_custom_card(self, keyword: str) -> Tuple[str, List[str]]:
        forbidden = self._auto_generate_forbidden(keyword)
        return keyword, forbidden

    def _auto_generate_forbidden(self, keyword: str) -> List[str]:
        forbidden = []
        if len(keyword) >= 2:
            for i in range(min(3, len(keyword))):
                if keyword[i] not in forbidden:
                    forbidden.append(keyword[i])
        if len(keyword) >= 2:
            for i in range(0, len(keyword) - 1):
                bigram = keyword[i:i + 2]
                if bigram not in forbidden and len(forbidden) < 3:
                    forbidden.append(bigram)
        forbidden = list(dict.fromkeys(forbidden))[:3]
        while len(forbidden) < 3:
            word = random.choice(CUSTOM_WORDS_STEM)
            if word not in forbidden and word != keyword:
                forbidden.append(word)
        return forbidden

    def clear_room(self, room_id: str):
        if room_id in self.used_words:
            del self.used_words[room_id]
